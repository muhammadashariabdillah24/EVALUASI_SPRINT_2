function hitungLuasPersegiPanjang(panjang,lebar){
    Panjang = panjang
    Lebar = lebar

    document.write(
        'Panjang : ',  Panjang + '<br>',
        'Lebar : ', Lebar + '<br><br>'
    )
    document.write(
        'Luas Persegi Panjang : ', Panjang * Lebar
    );
}

hitungLuasPersegiPanjang(15,5)

